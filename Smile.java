public class Smile {
    public static void main(String[] args) { 
        System.out.format("%12s", "*****\n");
        System.out.format("%6s %6s", "*", "*\n");
        System.out.format("%5s %1s %3s %1s", "*", "_", "_", "*\n");
        System.out.format("%4s %2s %3s %3s", "*", "O", "O", "*\n");
        System.out.format("%3s %5s %6s", "*", "|", "*\n");
        System.out.format("%3s %5s %6s", "*", "+", "*\n");
        System.out.format("");
    }
}
